package com.tnl.backend;

import java.util.*;

public class Exam {
	int examId;
	int code;
	String title;
	int categoryId;
	int duration;
	Account creatorExam;
	Date creatDate;
	
	public Exam () {}
	
	public Exam(int examId, int code, String title, int categoryId, int duration, Account creatorExam, Date creatDate) {
		
		this.examId = examId;
		this.code = code;
		this.title = title;
		this.categoryId = categoryId;
		this.duration = duration;
		this.creatorExam = creatorExam;
		this.creatDate = creatDate;
	}

	public int getExamId() {
		return examId;
	}

	public void setExamId(int examId) {
		this.examId = examId;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getCategoryId() {
		return categoryId;
	}

	public void setCategoryId(int categoryId) {
		this.categoryId = categoryId;
	}

	public int getDuration() {
		return duration;
	}

	public void setDuration(int duration) {
		this.duration = duration;
	}

	public Account getCreatorExam() {
		return creatorExam;
	}

	public void setCreatorExam(Account creatorExam) {
		this.creatorExam = creatorExam;
	}

	public Date getCreatDate() {
		return creatDate;
	}

	public void setCreatDate(Date creatDate) {
		this.creatDate = creatDate;
	}
	
	
}
