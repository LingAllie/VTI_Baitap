package com.tnl.entity;

public class MyMath {
	
	// Method to sum two integers
    public int sum(int a, int b) {
        return a + b;
    }

    // Method to sum two bytes
    public int sum(byte a, byte b) {
        return a + b;
    }

    // Method to sum two floats
    public float sum(float a, float b) {
        return a + b;
    }
}
